{
}

/**************************************************************/
void ios::setstate(iostate state)
/**************************************************************/
{
	clear(rdstate() | state);
}

/**************************************************************/
void ios::clear(iostate state)
/**************************************************************/
{
	_iostate = state;
}

/**************************************************************/
ios_base::iostate ios::rdstate()
/**************************************************************/
{
	return(_iostate);
}

/**************************************************************/
ios::operator void*()
/**************************************************************/
{
	if (bad())
		return false;
	else
		return (void*)true;

}

